    private void loadOrders() {
        OrderDAO orderDAO = new OrderDAO();
        List<Order> orders = orderDAO.getAllPaidOrders();
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID Order", "Tanggal", "Status", "Total Harga"}, 0);
        for (Order order : orders) {
            model.addRow(new Object[]{
                    order.getId(),
                    order.getTanggalOrder(),
                    order.getStatus(),
                    order.getTotalHarga()
            });
        }
        tblOrder.setModel(model);
        tblOrder.setDefaultEditor(Object.class, null);
    }

    private void loadOrderDetails(int orderId) {
        DetailOrderDAO detailOrderDAO = new DetailOrderDAO();
        MenuDAO menuDAO = new MenuDAO();  // Untuk mengambil nama menu berdasarkan ID
        List<DetailOrder> details = detailOrderDAO.getDetailsByOrderId(orderId);

        // Definisikan kolom yang akan ditampilkan pada tabel detail
        DefaultTableModel model = new DefaultTableModel(new String[]{"Nama Menu", "Jumlah", "Subtotal"}, 0);

        // Iterasi detail order dan ambil nama menu menggunakan ID menu
        for (DetailOrder detail : details) {
            String namaMenu = menuDAO.getMenuNameById(detail.getIdMenu());  // Ambil nama menu

            model.addRow(new Object[]{
                    namaMenu,  // Menampilkan nama menu
                    detail.getJumlah(),
                    detail.getSubtotal()
            });
        }

        tblDetailOrder.setModel(model);
        tblOrder.setDefaultEditor(Object.class, null);
    }

