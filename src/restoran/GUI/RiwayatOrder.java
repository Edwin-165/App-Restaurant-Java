    private void deleteOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteOrderActionPerformed
        int selectedRow = tblOrder.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih order untuk dihapus!");
            return;
        }

        int orderId = Integer.parseInt(tblOrder.getValueAt(selectedRow, 0).toString());
        OrderDAO orderDAO = new OrderDAO();
        orderDAO.deleteOrder(orderId);

        JOptionPane.showMessageDialog(this, "Order berhasil dihapus!");
        loadOrders();
    }//GEN-LAST:event_deleteOrderActionPerformed

    private void tblOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrderMouseClicked
        int selectedRow = tblOrder.getSelectedRow();
        if (selectedRow != -1) {
            // Ambil ID order dari kolom pertama (index 0)
            int orderId = Integer.parseInt(tblOrder.getValueAt(selectedRow, 0).toString());

            // Tampilkan detail order di tabel detail
            loadOrderDetails(orderId);
        }
    }//GEN-LAST:event_tblOrderMouseClicked

