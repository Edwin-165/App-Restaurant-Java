    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String namaKategori = txtNamaKategori.getText().trim();
        if (namaKategori.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama kategori tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Kategori kategori = new Kategori();
        kategori.setNamaKategori(namaKategori);

        KategoriDAO kategoriDAO = new KategoriDAO();
        kategoriDAO.create(kategori);

        JOptionPane.showMessageDialog(this, "Kategori berhasil ditambahkan!");
        loadKategoriTable();
        clearForm();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblKategori.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih kategori yang akan diupdate!");
            return;
        }

        int id = Integer.parseInt(tblKategori.getValueAt(selectedRow, 0).toString());
        String namaKategori = txtNamaKategori.getText().trim();
        if (namaKategori.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama kategori tidak boleh kosong!");
            return;
        }

        Kategori kategori = new Kategori(id, namaKategori);
        KategoriDAO kategoriDAO = new KategoriDAO();
        kategoriDAO.update(kategori);

        JOptionPane.showMessageDialog(this, "Kategori berhasil diperbarui!");
        loadKategoriTable();
        clearForm();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblKategori.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih kategori yang akan dihapus!");
            return;
        }

        int id = Integer.parseInt(tblKategori.getValueAt(selectedRow, 0).toString());
        Kategori kategori = new Kategori();
        kategori.setId(id);

        KategoriDAO kategoriDAO = new KategoriDAO();
        kategoriDAO.delete(kategori);

        JOptionPane.showMessageDialog(this, "Kategori berhasil dihapus!");
        loadKategoriTable();
        clearForm();
    }//GEN-LAST:event_btnDeleteActionPerformed

