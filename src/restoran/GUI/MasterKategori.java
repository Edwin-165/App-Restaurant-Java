    private void loadKategoriTable() {
        KategoriDAO kategoriDAO = new KategoriDAO();
        List<Kategori> kategoris = kategoriDAO.readAll();

        // Model tabel
        DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Nama Kategori"}, 0);
        for (Kategori kategori : kategoris) {
            model.addRow(new Object[]{kategori.getId(), kategori.getNamaKategori()});
        }
        tblKategori.setModel(model);
        tblKategori.setDefaultEditor(Object.class, null); // Non-editable
    }


